# Bool Type - Boolean values or Truth Values : {True and False}
i = 1
print("i = 1 ->",bool(i))  # True
i = 10
print("i = 10 ->",bool(i))  # True
i = 10.5
print("i = 10.5 ->",bool(i))  # True
i = 0.345
print("i = 0.345 ->",bool(i))  # True
i = 0
print("i = 0 ->",bool(i))  # False
i = 0.0
print("i = 0.0 ->",bool(i))  # False
i = 0 + 1j
print("i = 0 + 1j ->",bool(i))  # True
i = 0 + 0j
print("i = 0 + 0j ->",bool(i))  # Flase
i = True
print("i = True ->",bool(i))  # True
i = False
print("i = False ->",bool(i))  # False
i = "Pranavi"
print('i="Pranavi', bool(i))  # True
i = "O"
print('i="O"', bool(i))  # True
i = ""
print('i=""', bool(i))  # False



