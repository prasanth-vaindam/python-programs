# Basic or Fundamental DataTypes in Python
# All these are immutable - values cannot be changed in place
x = 300  # integer
y = 1+6j  # complex number
z = 9.8  # floating point numbers
name = "Prasanth Vaindam"  # string
name = "Prashanth Vaidnam"
is_raining = True
will_be_used_in_future = None

print(f"type of x: {x} is {type(x)}")
print(f"type of y: {y} is {type(y)}")
print(f"type of z: {z} is {type(z)}")
print(f"type of name: {name} is {type(name)}")
print(f"type of is_raining: {is_raining} is {type(is_raining)}")
print(f"type of will_be_used_in_future: {will_be_used_in_future} is {type(will_be_used_in_future)}")

