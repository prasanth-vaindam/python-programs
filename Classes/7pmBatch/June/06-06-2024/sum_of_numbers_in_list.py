# Program to find the sum of numbers in the given list

def sum_of_numbers(list_of_numbers):
    sum_total = 0
    for i in list_of_numbers:
        sum_total += i
    print(f"The Sum of all the numbers in the list is {sum_total}")


list_of_numbers = eval(input("Enter a list of numbers: "))

sum_of_numbers(list_of_numbers                                                                                                                                                                                               )